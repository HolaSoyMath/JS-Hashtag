// Para valores constantes, devemos utilizar o comando "CONST"
const mensagemConst = "Essa é a mensagem Constante";

// Para valores que podem variar, utilizamos o comando "LET"
let mensagemLet = "Essa é a mensagem alterável";

console.log(mensagemConst);
console.log(mensagemLet);

// Alterar variavel
mensagemLet = "Essa é a mensagem já alterada";

console.log(mensagemConst);
console.log(mensagemLet);
